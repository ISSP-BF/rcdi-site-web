/**
 * Prints Media Slider items
 */
function honeypress_print_admin_media_slider_items( $slider_items ) {

	foreach ( $slider_items as $slider_item ) {
		honeypress_print_admin_media_slider_item( $slider_item, '' );
	}

}

/**
 * Get Single Slider Media with ajax
 */
function honeypress_get_slider_media() {

	check_ajax_referer( 'honeypress-get-slider-media', '_honeypress_nonce' );

	if( isset( $_POST['attachment_ids'] ) ) {

		$attachment_ids = sanitize_text_field( $_POST['attachment_ids'] );

		if( !empty( $attachment_ids ) ) {

			$media_ids = explode(",", $attachment_ids);

			foreach ( $media_ids as $media_id ) {
				$slider_item = array (
					'id' => $media_id,
				);
				honeypress_print_admin_media_slider_item( $slider_item, "new" );
			}
		}
	}
	if( isset( $_POST['attachment_ids'] ) ) { die(); }
}
add_action( 'wp_ajax_honeypress_get_slider_media', 'honeypress_get_slider_media' );


/**
 * Prints Single Slider Media  Item
 */
function honeypress_print_admin_media_slider_item( $slider_item, $new = "" ) {
	$media_id = $slider_item['id'];

	$title = '';

	$thumb_src = wp_get_attachment_image_src( $media_id, 'thumbnail' );
	$thumbnail_url = $thumb_src[0];
	$alt = get_post_meta( $media_id, '_wp_attachment_image_alt', true );
	$alt = ! empty( $alt ) ? esc_attr( $alt ) : '';

	$honeypress_button_class = "honeypress-slider-item-delete-button";

	if( $new = "new" ) {
		$honeypress_button_class = "honeypress-slider-item-delete-button honeypress-item-new";
	}

?>
	<div class="honeypress-slider-item-minimal">
		<input class="<?php echo esc_attr( $honeypress_button_class ); ?> button" type="button" value="<?php esc_attr_e( 'Delete', 'honeypress' ); ?>">
		<h3 class="hndle honeypress-title">
			<span><?php esc_html_e( 'Image', 'honeypress' ); ?></span>
		</h3>
		<div class="inside">
			<input type="hidden" value="<?php echo esc_attr( $media_id ); ?>" name="honeypress_media_slider_item_id[]">
			<?php echo '<img class="honeypress-thumb" src="' . esc_url( $thumbnail_url ) . '" title="' . esc_attr( $title ) . '" attid="' . esc_attr( $media_id ) . '" alt="' . esc_attr( $alt ) . '" width="120" height="120"/>'; ?>
		</div>
	</div>
<?php

}

